package com.example.tinder.Model;

public class Relationship {
    private float id;
    private String status;


    // Getter Methods

    public float getId() {
        return id;
    }

    public String getStatus() {
        return status;
    }

    // Setter Methods

    public void setId(float id) {
        this.id = id;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
